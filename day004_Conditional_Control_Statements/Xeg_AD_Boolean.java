package day004_Conditional_Control_Statements;

public class Xeg_AD_Boolean {

	public static void main(String[] args) {

		// the context is to understand that we can either pass an expression or a
		// boolean value directly

		if (true)
			System.out.println(1);
		else
			System.out.println(2);

		if (false)
			System.out.println(1);
		else
			System.out.println(2);
	}

}
