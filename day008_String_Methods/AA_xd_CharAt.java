package day008_String_Methods;

public class AA_xd_CharAt {

	public static void main(String[] args) {
		// charAt()
		// Note - charAt() returns the character at certain index
		// Note - Index starts from 0

		String s = "this is a random string generated";
		System.out.println(s.charAt(5)); // i
		System.out.println(s.charAt(0)); // t
		System.out.println(s.charAt(25)); // e

	}

}
